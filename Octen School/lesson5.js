//--------------------------------------------------------//
let findMinNumber = (array) => {
    if (array.length === 0) {
        return "Масив порожній";
    }
    let min = array[0];
    for (let i = 1; i < array.length; i++) {
        if (array[i] < min) {
            min = array[i];
        }
    }
    return min;
}

let numbers = [5, 3, 9, 1, 7];
let minNumber = findMinNumber(numbers);
console.log("Найменше число у масиві:", minNumber);
